import type { IAIProvider } from "@/infrastructure/ai/IAIProvider";
import type { IGitHubClient } from "@/core/repositories/IGitHubClient";
import type { ISubscriptionRepository } from "@/core/repositories/ISubscriptionRepository";
import type { AuditFinding } from "@/core/entities/PullRequestAudit";
import {
  buildRepairMessages,
  parseRepairResponse,
} from "@/services/repair/RepairPromptBuilder";
import { ok, err } from "@/lib/types";
import type { Result } from "@/lib/types";

export interface AutoFixInput {
  organizationId: string;
  repoFullName: string;
  prNumber: number;
  headSha: string;
  baseBranch: string;
  criticalFindings: AuditFinding[];
  installationToken: string;
  aiModel: string;
}

export interface AutoFixResult {
  fixPrNumber: number;
  branchName: string;
  patchedFileCount: number;
}

interface FilePatch {
  filePath: string;
  patchedContent: string;
  originalSha: string;
  changesSummary: string;
}

function groupByFile(findings: AuditFinding[]): Map<string, AuditFinding[]> {
  const map = new Map<string, AuditFinding[]>();
  for (const f of findings) {
    const existing = map.get(f.filePath) ?? [];
    existing.push(f);
    map.set(f.filePath, existing);
  }
  return map;
}

function buildPrBody(
  originalPrNumber: number,
  findings: AuditFinding[],
  patchedFiles: number
): string {
  const list = findings
    .map(
      (f) =>
        `- **[${f.severity.toUpperCase()}]** ${f.title} in \`${f.filePath}\` (Lines ${f.lineStart}–${f.lineEnd})`
    )
    .join("\n");

  return [
    "## 🛡 RepoShield AutoFix — Automated Security Patch",
    "",
    `This PR was automatically generated in response to **${findings.length} critical vulnerability(s)** detected in PR #${originalPrNumber}.`,
    "",
    "### Vulnerabilities Fixed",
    list,
    "",
    "### Changes",
    `- Patched **${patchedFiles} file(s)** with minimal, syntax-preserving fixes`,
    "- All patches follow OWASP Top 10 remediation guidelines",
    "",
    "> ⚡ Generated by [RepoShield AI AutoFix](https://reposhield.ai) — Enterprise Self-Healing Engine",
  ].join("\n");
}

export class AutoFixEngine {
  constructor(
    private readonly aiProvider: IAIProvider,
    private readonly githubClient: IGitHubClient,
    private readonly subscriptionRepo: ISubscriptionRepository
  ) {}

  triggerIfEligible(input: AutoFixInput): void {
    this.execute(input).catch(() => {
      // Fire-and-forget: auto-fix is best-effort and must never crash the audit pipeline
    });
  }

  async execute(
    input: AutoFixInput
  ): Promise<Result<AutoFixResult | null>> {
    // 1. Verify enterprise plan
    const subResult = await this.subscriptionRepo.findByOrganizationId(
      input.organizationId
    );
    if (!subResult.success) return subResult;
    if (!subResult.data || subResult.data.planType !== "enterprise") {
      return ok(null);
    }

    // 2. Filter to critical severity only
    const critical = input.criticalFindings.filter(
      (f) => f.severity === "critical"
    );
    if (critical.length === 0) return ok(null);

    // 3. Generate patches per file
    const byFile = groupByFile(critical);
    const patches: FilePatch[] = [];

    for (const [filePath, fileFindings] of byFile.entries()) {
      const contentResult = await this.githubClient.getFileContent(
        input.repoFullName,
        filePath,
        input.headSha,
        input.installationToken
      );
      if (!contentResult.success) continue; // best-effort per file

      const patchResult = await this.generatePatch(
        filePath,
        contentResult.data.content,
        fileFindings,
        input.aiModel
      );
      if (!patchResult.success) continue;

      patches.push({
        filePath,
        patchedContent: patchResult.data.patchedContent,
        originalSha: contentResult.data.sha,
        changesSummary: patchResult.data.changesSummary,
      });
    }

    if (patches.length === 0) return ok(null);

    // 4. Create fix branch from head SHA
    const branchName = `reposhield/autofix-pr-${input.prNumber}`;
    const branchResult = await this.githubClient.createBranch(
      input.repoFullName,
      branchName,
      input.headSha,
      input.installationToken
    );
    if (!branchResult.success) return branchResult;

    // 5. Commit each patched file onto the fix branch
    for (const patch of patches) {
      await this.githubClient.createOrUpdateFile(
        input.repoFullName,
        patch.filePath,
        patch.patchedContent,
        `fix(autofix): ${patch.changesSummary}`,
        branchName,
        patch.originalSha,
        input.installationToken
      );
    }

    // 6. Open fix PR targeting the original PR's base branch
    const prTitle = `[RepoShield Auto-Fix] Security & Debt Patch for #${input.prNumber}`;
    const prBody = buildPrBody(input.prNumber, critical, patches.length);
    const prResult = await this.githubClient.createPullRequest(
      input.repoFullName,
      prTitle,
      prBody,
      branchName,
      input.baseBranch,
      input.installationToken
    );
    if (!prResult.success) return prResult;

    return ok({
      fixPrNumber: prResult.data,
      branchName,
      patchedFileCount: patches.length,
    });
  }

  private async generatePatch(
    filePath: string,
    fileContent: string,
    findings: AuditFinding[],
    model: string
  ): Promise<Result<{ patchedContent: string; changesSummary: string }>> {
    try {
      const messages = buildRepairMessages(filePath, fileContent, findings);
      const completion = await this.aiProvider.complete({
        model,
        messages,
        maxTokens: 4096,
        temperature: 0.05,
        responseFormat: "json_object",
      });
      const parsed = parseRepairResponse(completion.content);
      return ok(parsed);
    } catch (error) {
      return err(error instanceof Error ? error : new Error(String(error)));
    }
  }
}
